def _month_coordinate(time: Any) -> float:
    """Return a fractional month coordinate in the interval [1, 13)."""
    if isinstance(time, np.datetime64):
        day = time.astype("datetime64[D]").astype(object)
        if isinstance(day, int):
            msg = "np.datetime64 values must include a calendar date"
            raise TypeError(msg)
        time = day

    if isinstance(time, _dt.datetime | _dt.date):
        days_in_month = calendar.monthrange(time.year, time.month)[1]
        return time.month + (time.day - 1) / days_in_month

    value = float(time)
    if 1.0 <= value < 13.0:
        return value

    msg = "time must be a date/datetime/np.datetime64 or a fractional month in [1, 13)"
    raise ValueError(msg)


def _interpolate_month(table: np.ndarray, month_coordinate: float) -> np.ndarray:
    lower = int(np.floor(month_coordinate))
    fraction = month_coordinate - lower
    lower_index = (lower - 1) % 12
    upper_index = lower % 12
    return (1.0 - fraction) * table[lower_index] + fraction * table[upper_index]


def number_density(
    altitudes_m: npt.ArrayLike,
    time: _dt.datetime | _dt.date | np.datetime64 | float,
    latitude_degrees: float,
    lower_fill_value: float | None = 0.0,
    upper_fill_value: float | None = None,
) -> np.ndarray:
    """Return atomic oxygen number density on an altitude grid.

    Parameters
    ----------
    altitudes_m : array-like
        Altitudes in metres. Values must be within 40 km to 130 km.
    time : datetime, date, np.datetime64, or float
        Time used for interpolation. Numeric values are interpreted as fractional
        month numbers, where 1 is January and 12 is December. Values between
        month integers are linearly interpolated, with December wrapping to January.
    latitude_degrees : float
        Geographic latitude in degrees. Values must be within -80 to 80 degrees.
    lower_fill_value : float or None, optional
        Number density in m^-3 to use below 40 km. If None, the 40 km value is
        used. The default is 0.
    upper_fill_value : float or None, optional
        Number density in m^-3 to use above 130 km. If None, the 130 km value is
        used. The default is None.

    Returns
    -------
    np.ndarray
        Atomic oxygen number density in m^-3, with the same shape as ``altitudes_m``.
    """
    altitude_array = np.asarray(altitudes_m, dtype=float)
    latitude = float(latitude_degrees)

    if latitude < LATITUDES_DEGREES.min() or latitude > LATITUDES_DEGREES.max():
        msg = "latitude_degrees must be within the tabulated range -80 to 80 degrees"
        raise ValueError(msg)

    table_at_time = _interpolate_month(_TABLE_CM3, _month_coordinate(time))

    profile_cm3 = np.empty(table_at_time.shape[0], dtype=float)
    for altitude_index in range(table_at_time.shape[0]):
        profile_cm3[altitude_index] = np.interp(
            latitude, LATITUDES_DEGREES, table_at_time[altitude_index]
        )

    lower_fill_cm3 = (
        profile_cm3[-1] if lower_fill_value is None else float(lower_fill_value) / 1.0e6
    )
    upper_fill_cm3 = (
        profile_cm3[0] if upper_fill_value is None else float(upper_fill_value) / 1.0e6
    )

    density_cm3 = np.interp(
        altitude_array,
        ALTITUDES_M[::-1],
        profile_cm3[::-1],
        left=lower_fill_cm3,
        right=upper_fill_cm3,
    )
    return density_cm3 * 1.0e6
